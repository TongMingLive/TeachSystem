public class Answering {

    private int answeringId;
    private String answeringTitle;
    private String answeringPage;
    private String answeringQuestion;
    private String userNumber;
    private String userName;

    public int getAnsweringId() {
        return answeringId;
    }

    public void setAnsweringId(int answeringId) {
        this.answeringId = answeringId;
    }

    public String getAnsweringTitle() {
        return answeringTitle;
    }

    public void setAnsweringTitle(String answeringTitle) {
        this.answeringTitle = answeringTitle;
    }

    public String getAnsweringPage() {
        return answeringPage;
    }

    public void setAnsweringPage(String answeringPage) {
        this.answeringPage = answeringPage;
    }

    public String getAnsweringQuestion() {
        return answeringQuestion;
    }

    public void setAnsweringQuestion(String answeringQuestion) {
        this.answeringQuestion = answeringQuestion;
    }

    public String getUserNumber() {
        return userNumber;
    }

    public void setUserNumber(String userNumber) {
        this.userNumber = userNumber;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }
}
